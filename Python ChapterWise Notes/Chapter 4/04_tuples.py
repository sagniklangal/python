#Tuple creation using ()
t = (1,2,6,8,9) # Normal tuple
t1 = () # Empty tuple
t2 = (1) # Wrong way of declaring a tuple with single element 
t3 = (1,) # Correct way of declaring a tuple with single element

#printing values
print(t)
print(t1)
print(t2)
print(t3)

